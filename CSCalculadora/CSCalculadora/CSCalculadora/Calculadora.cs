using System;
using CSCalculadora;
using Gtk;

public class Calculadora
{
    public float suma(float num1, float num2)
    {
        return num1 + num2;
    }
    public float resta(float num1, float num2)
    {
        return num1 - num2;
    }
    public float producto(float num1, float num2)
    {
        return num1 * num2;
    }
    public float cociente(float num1, float num2)
    {
        return num1 / num2;
    }


}